function getHolidaysByDate(date) {
    console.log()
    let holidayList = [];
    let yearOfOurLord = 2023;
    let lastYearOfData = 2033;

    if (date.getFullYear() > lastYearOfData) {return holidayList;};
    
    let yearIndex = date.getFullYear() - yearOfOurLord + 1; // [0] is a number of days

    // this is a for loop
    for (let holiday in HOLIDAYS) {

        const endHoliday = new Date(HOLIDAYS[holiday][yearIndex].getTime() + (HOLIDAYS[holiday][0] * 24 * 60 * 60 * 1000));

        if (dayIsBetween(date, HOLIDAYS[holiday][yearIndex], endHoliday)) {
            holidayList.push(holiday); continue;
        }
        // adding a special case in, in case it goes over the current year
        if (endHoliday.getFullYear() != HOLIDAYS[holiday][yearIndex].getFullYear()) {
           
            if (date.getFullYear() == yearOfOurLord) {continue};
            
            const endHolidayAlt = new Date(HOLIDAYS[holiday][yearIndex -1].getTime() + (HOLIDAYS[holiday][0] * 24 * 60 * 60 * 1000));
            console.log(date, HOLIDAYS[holiday][yearIndex -1], endHolidayAlt)
            if (dayIsBetween(date, HOLIDAYS[holiday][yearIndex -1], endHolidayAlt)) {
                
                holidayList.push(holiday); continue;
                
            }
        }
    
    }
    return holidayList;

}

